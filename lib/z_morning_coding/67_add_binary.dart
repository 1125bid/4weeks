class Solution {
  String addBinary(String a, String b) {
    int deca = int.parse(a, radix: 2); //10진법으로 교체
    int decb = int.parse(b, radix: 2); //10진법으로 교체
    int sum = deca + decb; //합
    return sum.toRadixString(2);
//2진법으로 교체
  }
}

//     - **뒤에서부터 덧셈**:
//     - `a`와 `b`의 끝에서부터 한 자리씩 더하면서 올림(carry)를 고려합니다.
// - **각 자리의 합 구하기**:
//     - 현재 자리의 `a`와 `b` 값을 더하고, 이전 자리에서 넘어온 올림을 추가합니다.
//     - 합이 2 이상이면 올림을 `1`로 설정하고, 그렇지 않으면 `0`으로 설정합니다.
// - **결과 문자열에 추가**:
//     - 계산된 각 자리의 결과를 문자열 앞에 추가하여 최종 결과를 만듭니다.
// - **올림 처리**:
//     - 모든 자리를 다 계산한 후에도 올림이 `1`이면 결과 문자열 앞에 `1`을 추가합니다.
